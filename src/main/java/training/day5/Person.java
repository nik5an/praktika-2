package training.day5;

public class Person {
    private String name;
    private String gender;
    private String id;

    public Person(String name, String gender, String id) {
        this.name = name;
        this.gender = gender;
        this.id = id;
    }
    public Person() {
        this.name = "Gosho";
        this.gender = "Male";
        this.id = "69";
    }
}
